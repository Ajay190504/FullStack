public class ClassTemp {

    public static void main(String[] args) {

        int temp = 18;

        if(temp >= 35){
            System.out.println("Hot");
        }
        else if(temp >= 25){
            System.out.println("Warm");
        }
        else if(temp >= 15){
            System.out.println("Cool");
        }
        else{
            System.out.println("Cold");
        }
    }
}